package fast.dev.platform.android.bean;

public class DictData {

	private int ID;
	private int UP_ID;
	private String DICT_NAME;
	private int DICT_LEVEL;

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}

	public int getUP_ID() {
		return UP_ID;
	}

	public void setUP_ID(int uP_ID) {
		UP_ID = uP_ID;
	}

	public String getDICT_NAME() {
		return DICT_NAME;
	}

	public void setDICT_NAME(String dICT_NAME) {
		DICT_NAME = dICT_NAME;
	}

	public int getDICT_LEVEL() {
		return DICT_LEVEL;
	}

	public void setDICT_LEVEL(int dICT_LEVEL) {
		DICT_LEVEL = dICT_LEVEL;
	}

}
